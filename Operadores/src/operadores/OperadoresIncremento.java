public class OperadoresIncremento {
    public static void main(String[] args) {
        
        int x = 5;
        
        x++;
        x--;
        
        System.out.println("x " + x);
        
        int y = 5, z;
        
        z = ++y;
        z = --y;
        
        System.out.println("Z = " + z);
        
    }
}
